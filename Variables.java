package com.variables;

public class Variables {
	
	int age;
	int Employeenumber;
	

	public static void main(String[] args) {
		
		// Primitive variables
		
		Variables V = new Variables();
		 
		V.age=12;
		V.Employeenumber = 1245;
		
		System.out.println(V.age);
		System.out.println(V.Employeenumber);
		
	
	}

}
