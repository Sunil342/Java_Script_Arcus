package com.Switch;

public class SwitchCase {

	public static void main(String[] args) {

		int number=5;


		switch(number) {

		case 1: number=1 ;

		System.out.println("The number is :" +number);

		break;

		case 2: number=2 ;

		System.out.println("The number is :" +number);

		break;

		case 3: number=5 ;

		System.out.println("The number is :" +number);

		break;


		case 4: number=4 ;

		System.out.println("The number is :" +number);

		break;

		case 5: number=5;

		System.out.println("The number is :" +number);

		break;

		case 6: number=6 ;

		System.out.println("The number is :" +number);

		break;

		case 7: number=7 ;

		System.out.println("The number is :" +number);

		break;
		case 8: number=8 ;

		System.out.println("The number is :" +number);

		break;

		case 9: number=1 ;

		System.out.println("The number is :" +number);

		break;
		case 10: number=1 ;

		System.out.println("The number is :" +number);

		break;
		
		default:
			
			System.out.println("Incorrect value");
		}
		
		//System.out.println("The Given number is " +number);

	}

}
