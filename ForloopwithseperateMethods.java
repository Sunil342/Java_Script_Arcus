package com.forloop;

public class ForloopwithseperateMethods {
	
	public void printnumbers() {
		
		for(int i=1;i<=5;i++)
			System.out.println(i);
	}

	public static void main(String[] args) {
		
		ForloopwithseperateMethods F=new ForloopwithseperateMethods();
		
		F.printnumbers();
	

	}

}
