package com.Array;

public class SingleDimensionalArray {

	public static void main(String[] args) {
		
		String Array[] = new String[5];
		
		Array[0] = "Hyderabad";
		Array[1] = "Vijayawada";
		Array[2] = "Chirala";
		Array[3] = "Guntur";
		Array[4] = "Palnadu";
		
		for(int i =0; i<Array.length;i++) {
			
			System.out.println(Array[i]);
		}
				
	}

}
